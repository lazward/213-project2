public class Profile {

    private String fname ;
    private String lname ;
    
    public String getFirstName(){
        return this.fname;
    }
    public String getLastName(){
        return this.lname;
    }

    public void setFirstName(String first) {

        fname = first ;

    }

    public void setLastName(String last) {

        lname = last ;

    }
}


